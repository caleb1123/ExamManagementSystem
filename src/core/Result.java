package core;

/**
 *
 * @author ryy15
 */
public class Result {
    String studentId;
    double score;
    String examID;

    public Result(String studentId, double score, String examID) {
        this.studentId = studentId;
        this.score = score;
        this.examID = examID;
    }

    public String getStudentId() {
        return studentId;
    }

    public void setStudentId(String studentId) {
        this.studentId = studentId;
    }

    public double getScore() {
        return score;
    }

    public void setScore(double score) {
        this.score = score;
    }

    public String getExamID() {
        return examID;
    }

    public void setExamID(String examID) {
        this.examID = examID;
    }

    

}
